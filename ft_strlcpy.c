int	strlcpy(char *dest, const char *src, int size)
{
	int	i;

	i = 0;
	while (i < (size - 1))
	{
		*(dest + i) = *(src + i);
		i++;
	}
	*(dest + i) = '\0';
	i = 0;
	while (*(src + i) != '\0')
		i++;
	return (i);
}
